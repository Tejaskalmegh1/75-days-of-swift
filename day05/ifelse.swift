// Day 5: if-else conditions in Swift

let age = 20

if age >= 18 {
    print("You are eligible to vote.")
} else {
    print("You are not eligible to vote.")
}

let marks = 72

if marks >= 90 {
    print("Grade: A")
} else if marks >= 75 {
    print("Grade: B")
} else if marks >= 60 {
    print("Grade: C")
} else {
    print("Grade: D")
}

// Boolean condition
let isLoggedIn = true

if isLoggedIn {
    print("Welcome back!")
} else {
    print("Please log in.")
}
