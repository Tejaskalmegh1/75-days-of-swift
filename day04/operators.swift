// Day 4: Operators in Swift

// Arithmetic operators
let a = 10
let b = 3

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Remainder:", a % b)

// Comparison operators
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)

// Logical operators
let isLoggedIn = true
let isAdmin = false

print("isLoggedIn AND isAdmin:", isLoggedIn && isAdmin)
print("isLoggedIn OR isAdmin:", isLoggedIn || isAdmin)
print("NOT isAdmin:", !isAdmin)
