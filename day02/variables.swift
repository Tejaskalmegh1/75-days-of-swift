// Day 2: Variables and Constants

// Constant (cannot change)
let name = "Tejas Patil"
let age = 25

print("Name:", name)
print("Age:", age)

// Variable (can change)
var score = 99
print("Initial score:", score)

// Updating variable
score = 100
print("Updated score:", score)

// Example with calculation
let year = 2026
var birthYear = 2001

let currentAge = year - birthYear
print("Calculated age:", currentAge)
