// Day 7: Loops in Swift

// for-in loop
for number in 1...7 {
    print("Number:", number)
}

// for-in with array
let fruits = ["Apple", "Banana", "Mango"]

for fruit in fruits {
    print("Fruit:", fruit)
}

// while loop
var counter = 1

while counter <= 10 {
    print("Counter:", counter)
    counter += 1
}
